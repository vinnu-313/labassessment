    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        int x=JOptionPane.showConfirmDialog(rootPane, "Are you sure want to reset ? Resetting will delete all the viva questions and related.", "Reset Viva Questions - Lab Assessment System v1.0b", JOptionPane.YES_NO_OPTION);
        if(x==JOptionPane.YES_OPTION){
            try {
                Connection con=MyConnection.getConnection();
                Statement st=con.createStatement();
                st.executeUpdate("delete from vivamap");
                st.executeUpdate("delete from viva");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(VivaQuestionView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(VivaQuestionView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        questionTextArea.setText("");
    }//GEN-LAST:event_resetButtonActionPerformed

