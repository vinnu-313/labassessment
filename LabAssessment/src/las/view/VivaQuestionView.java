    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if(questionTextArea.getText().length()>5){
            try {
                Connection con=MyConnection.getConnection();
                PreparedStatement ps=con.prepareStatement("insert into viva(question) values(?)");
                ps.setString(1, questionTextArea.getText());
                ps.executeUpdate();
                warningLabel.setText("Viva Question Added....!");
                questionTextArea.setText("");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(VivaQuestionView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(VivaQuestionView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            warningLabel.setText("Question too short...!");
        }
    }//GEN-LAST:event_addButtonActionPerformed

