    private void fetchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fetchButtonActionPerformed
        try {
            Connection con = MyConnection.getConnection();
            PreparedStatement ps = con.prepareCall("select * from batch where idbatch = ?");
            ps.setInt(1, Integer.parseInt(currentBatchTextField.getText()));
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                currentBatchTextField.setText(rs.getString(1));
                subjectTextField.setText(rs.getString(2));
                descriptionTextArea.setText(rs.getString(3));
            } else {
                warningLabel.setForeground(Color.red);
                warningLabel.setText("No current batch available");
            }
        } catch (Exception e) {
            warningLabel.setForeground(Color.red);
            warningLabel.setText("Invalid Batch Number.!");
        }
    }//GEN-LAST:event_fetchButtonActionPerformed

    private void currentBatchTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_currentBatchTextFieldFocusLost
        try {
            Connection con = MyConnection.getConnection();
            PreparedStatement ps = con.prepareCall("select * from batch where idbatch = ?");
            ps.setInt(1, Integer.parseInt(currentBatchTextField.getText()));
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                currentBatchTextField.setText(rs.getString(1));
                subjectTextField.setText(rs.getString(2));
                descriptionTextArea.setText(rs.getString(3));
                changeButton.setEnabled(true);
            } else {
                subjectTextField.setText("");
                warningLabel.setForeground(Color.red);
                descriptionTextArea.setText("");
                warningLabel.setText("No current batch available");
                changeButton.setEnabled(false);
            }
        } catch (Exception e) {
            warningLabel.setForeground(Color.red);
            warningLabel.setText("Invalid Batch Number.!");
        }
    }//GEN-LAST:event_currentBatchTextFieldFocusLost

    private void changeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeButtonActionPerformed
        try {
            Connection con = MyConnection.getConnection();
            PreparedStatement ps = con.prepareCall("update batch set current = ?");
            ps.setBoolean(1, false);
            ps.executeUpdate();
            ps = con.prepareCall("update batch set current = ? where idbatch = ?");
            ps.setBoolean(1, true);
            ps.setInt(2, Integer.parseInt(currentBatchTextField.getText()));
            ps.executeUpdate();
            warningLabel.setForeground(Color.green);
            warningLabel.setText("Current Batch Changed !");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
            warningLabel.setText("Unable to Change the Current Batch...!");
            warningLabel.setForeground(Color.red);
        } catch (SQLException ex) {
            Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
            warningLabel.setText("Unable to Change the Current Batch...!");
            warningLabel.setForeground(Color.red);
        } catch (Exception ex) {
            Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
            warningLabel.setText("Invalid Batch Number ");
            warningLabel.setForeground(Color.red);
        }
    }//GEN-LAST:event_changeButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (subjectTextField.getText().length() > 0) {
            try {
                Connection con = MyConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("insert into batch(subject, description, current) values(?,?,?)", PreparedStatement.RETURN_GENERATED_KEYS);
                ps.setString(1, subjectTextField.getText());
                ps.setString(2, descriptionTextArea.getText());
                ps.setBoolean(3, false);
                ps.executeUpdate();
                ResultSet rs = ps.getGeneratedKeys();
                if (rs.next()) {
                    warningLabel.setForeground(Color.green);
                    warningLabel.setText("Batch Added ! Batch ID : " + rs.getInt(1));
                    subjectTextField.setText("");
                    descriptionTextArea.setText("");
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
                warningLabel.setText("Unable to add new batch... !");
                warningLabel.setForeground(Color.red);
            } catch (SQLException ex) {
                Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
                warningLabel.setText("Unable to add new batch... !");
                warningLabel.setForeground(Color.red);
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

