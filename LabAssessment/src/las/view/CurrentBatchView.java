    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            Connection con = MyConnection.getConnection();
            PreparedStatement ps = con.prepareCall("select * from batch where current = ?");
            ps.setBoolean(1, true);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                currentBatchTextField.setText(rs.getString(1));
                subjectTextField.setText(rs.getString(2));
                descriptionTextArea.setText(rs.getString(3));
            } else {
                warningLabel.setForeground(Color.red);
                warningLabel.setText("No current batch available");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CurrentBatchView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

